print(2**3) # 2^3 = 8
print(5%3) # 나머지 구하기 2
print(10%3) # 1
print(5//3) # 몫 구하기 1
print(10//3) # 3

print(10 > 3) # True
print(4 >= 7) # False

print(3==3) # 같다 True
print(4==2) # False
print(3+4 == 7) # True

print(1 != 3) # 같지 않다 True 
print(not(1 != 3)) # False

print((3>0) and (3<5)) # True
print((3>0) & (3<5)) # & = and 정답은 True

print(((3>0) or (3>5))) # True 하나만 맞아도 True
print(((3>0) | (3>5))) # | = or 정답은 True